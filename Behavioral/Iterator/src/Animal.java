
public class Animal {
	
	private String nome;
	private String classe;
	
	public Animal(String nome, String classe){
		this.nome = nome;
		this.classe = classe;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getClasse() {
		return classe;
	}

	public void setClasse(String classe) {
		this.classe = classe;
	}
	
}
